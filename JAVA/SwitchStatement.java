package com.ust.examples;

public class SwitchStatement {

	public static void main(String[] args) {
		
		int color = 5;
		switch(color){
			
			case 1 : System.out.println("Color is Red");
		    break;
			
			case 2 : System.out.println("Color is Blue");
			break;
			
			case 3 : System.out.println("Color is Green");
			break;
			
			case 4 : System.out.println("Color is Yellow");
			break;
			
			case 5 : System.out.println("Color is Black");
			break;
			
			case 6 : System.out.println("Color is White");
			break;
			
			case 7 : System.out.println("Color is Orange");
			break;
			
			
			default : System.out.println("Color is not found");		
			
			
			
		}

	}

}
